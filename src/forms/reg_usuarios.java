    void registrar(){
        String usuario = null, contra = null;
        usuario = txt_usuario.getText().toLowerCase();
        contra = txt_contra.getText().toLowerCase();
        
        if (usuario.isEmpty() || contra.isEmpty()) {
            txt_usuario.requestFocus();
        } else {
            
            
              String sql = "Insert into usuario(nickname,password) "
                           + "Values('" + usuario + "','" + contra +"')";
              Funciones.guardar_datos(sql);
              limpiar();
               
        }
        
    }
    void limpiar() {
        txt_usuario.requestFocus();
        txt_usuario.setText("");
        txt_contra.setText("");
    }
